#!/usr/bin/env node
import { readFileSync, writeFileSync, readdirSync, existsSync } from "node:fs";
import { join } from "node:path";

const TARGETS = [
  { root: "docs/first-60-days", out: "src/features/first-60-days/summaries.ts" },
  { root: "docs/next-60-days", out: "src/features/next-60-days/summaries.ts" },
];

const FRONT_RE = /^---\s*\n([\s\S]*?)\n---/;
const REFLECTION_RE = /^##\s+(Reflection worksheet|Final reflection[^\n]*)$/m;

function buildSummaries(root, out) {
  if (!existsSync(root)) {
    console.log(`skipping ${root} — not found`);
    return;
  }
  const weeks = readdirSync(root)
    .filter((d) => d.startsWith("week-"))
    .sort((a, b) => Number(a.slice(5)) - Number(b.slice(5)));

  const summaries = [];
  for (const w of weeks) {
    const files = readdirSync(join(root, w))
      .filter((f) => /^day-\d+\.md$/.test(f))
      .sort();
    for (const f of files) {
      const raw = readFileSync(join(root, w, f), "utf8");
      const m = raw.match(FRONT_RE);
      if (!m) continue;
      const yaml = m[1];
      const get = (k) => {
        const mm = yaml.match(new RegExp("^" + k + ":\\s*(.*)$", "m"));
        if (!mm) return null;
        return mm[1].trim().replace(/^["']|["']$/g, "");
      };
      const day = Number(get("day") ?? 0);
      const week = Number(get("week") ?? 0);
      const title = get("title") ?? "";
      const duration = Number(get("duration_minutes") ?? 20);
      const hasReflection = REFLECTION_RE.test(raw);
      summaries.push({
        dayNumber: day,
        week,
        dayInWeek: ((day - 1) % 6) + 1,
        title,
        duration,
        hasReflection,
      });
    }
  }
  summaries.sort((a, b) => a.dayNumber - b.dayNumber);

  const body = [
    "// AUTO-GENERATED by scripts/build-day-summaries.mjs. Do not edit by hand.",
    `// Regenerate after changing day markdown files: node scripts/build-day-summaries.mjs`,
    `// Source: ${root}`,
    "",
    "export type DaySummary = {",
    "  dayNumber: number;",
    "  week: number;",
    "  dayInWeek: number;",
    "  title: string;",
    "  duration: number;",
    "  hasReflection: boolean;",
    "};",
    "",
    "export const DAY_SUMMARIES: DaySummary[] = " +
      JSON.stringify(summaries, null, 2) +
      ";",
    "",
    "export const TOTAL_DAYS = DAY_SUMMARIES.length;",
    "",
    "export const DAYS_WITH_REFLECTION: ReadonlySet<number> = new Set(",
    "  DAY_SUMMARIES.filter((d) => d.hasReflection).map((d) => d.dayNumber),",
    ");",
    "",
  ].join("\n");

  writeFileSync(out, body);
  console.log(`wrote ${summaries.length} summaries to ${out}`);
}

for (const t of TARGETS) {
  buildSummaries(t.root, t.out);
}
